function describe_city(nameOfCity, country) {
    return "".concat(nameOfCity, " is in ").concat(country);
}
var city1 = describe_city("Karachi", "Pakistan");
var city2 = describe_city("Seoul", "Korea");
var city3 = describe_city("Ankara", "Turkey");
var city4 = describe_city("Makkah", "Saudia Arab");
console.log(city1);
console.log(city2);
console.log(city3);
console.log(city4);
